class Song {
    private String type,artist,name;

    public Song(String type, String artist, String name) {
        this.type = type;
        this.artist = artist;
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public String getArtist() {
        return artist;
    }

    public String getName() {
        return name;
    }


    
    
    
    
}
